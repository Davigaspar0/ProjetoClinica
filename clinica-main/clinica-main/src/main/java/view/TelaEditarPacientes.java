    private void bTnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTnEditarActionPerformed
       
           
        if ( txtNome.getText().equals("") &&
        txtconv.getText().equals("") &&
        txtCPF.getText().equals("") &&
        txtEndereço.getText().equals("") &&
        txtsexo.getText().equals("")&&
        txtRG.getText().equals("") &&
        txttelefone.getText().equals("") &&
        txtCPF.getText().equals("")){
             JOptionPane.showMessageDialog(null, "PREENCHA OS CAMPOS!");
               return;
        }
        

       String nome,convenio,cpf,rg,endereço,telefone,sexo;
       
       int id = Integer.parseInt(txtID.getText());
       
       nome = txtNome.getText();
       convenio = txtconv.getText();
       cpf = txtCPF.getText();
       rg = txtRG.getText();
       endereço = txtEndereço.getText();
       telefone = txttelefone.getText();
       sexo = txtsexo.getText();
       
       Paciente objPacEditar = new Paciente();
       
       objPacEditar.setId(id);
       objPacEditar.setNome(nome);
       objPacEditar.setConvenio(convenio);
       objPacEditar.setEndereco(endereço);
       objPacEditar.setCpf(cpf);
       objPacEditar.setRg(rg);
       objPacEditar.setSexo(sexo);
       objPacEditar.setTelefone(telefone);
       
       PacienteDAO objPacDao = new PacienteDAO();
       
       try {
         String idTexto = txtID.getText();

        if (idTexto.equals("")) {
        JOptionPane.showMessageDialog(null, "PARA EDITAR COLOQUE O ID!");
        return;
        }

   
            objPacDao.editarPaciente(objPacEditar);
            
             JOptionPane.showMessageDialog(null, "Editado com sucesso!");   
       } catch  (SQLException ex) {
           Logger.getLogger(TelaEditarPacientes.class.getName()).log(Level.SEVERE, null, ex);
       }
    
    }//GEN-LAST:event_bTnEditarActionPerformed

