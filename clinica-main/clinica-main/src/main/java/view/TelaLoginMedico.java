    private void BtnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEntrarActionPerformed
        String nome,senha;
      
        nome = txtNome.getText();
        senha = txtSenha.getText();
        
        if (nome.equals("") && senha.equals("")){
            JOptionPane.showMessageDialog(null, "Preencha os campos!");
            return;
        }
        
         Medicos objmedLogin = new Medicos();
         
         objmedLogin.setNome_medico(nome);
         objmedLogin.setSenha_de_acesso(senha);
        
        MedicosDAO objmedDAOLOGIN = new MedicosDAO();
        
        try {
            Integer id =
            objmedDAOLOGIN.Loginmed(objmedLogin );
           if (id != null){
               
            TelaCadastroSecretaria cad2 = new TelaCadastroSecretaria();
               cad2.setVisible(true);
               dispose();
               
             
           }else {
               JOptionPane.showMessageDialog(null, "usuario ou senha incorretos!");
           }
               
       } catch (SQLException ex) {
          JOptionPane.showMessageDialog(null, "error no sistema" );

       }
    }//GEN-LAST:event_BtnEntrarActionPerformed

