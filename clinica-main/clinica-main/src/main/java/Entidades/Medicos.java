public class Medicos {
    private Integer id;
    private String Crm;
    private String nome_medico;
    private String rg;
    private String cpf;
    private String telefone;
    private String sexo;
    private String endereco;
    private String senha_de_acesso;

    /**
     * @return the id
     */
    public Integer getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * @return the Crm
     */
    public String getCrm() {
        return Crm;
    }

    /**
     * @param Crm the Crm to set
     */
    public void setCrm(String Crm) {
        this.Crm = Crm;
    }

    /**
     * @return the nome_medico
     */
    public String getNome_medico() {
        return nome_medico;
    }

    /**
     * @param nome_medico the nome_medico to set
     */
    public void setNome_medico(String nome_medico) {
        this.nome_medico = nome_medico;
    }

    /**
     * @return the rg
     */
    public String getRg() {
        return rg;
    }

    /**
     * @param rg the rg to set
     */
    public void setRg(String rg) {
        this.rg = rg;
    }

    /**
     * @return the cpf
     */
    public String getCpf() {
        return cpf;
    }

    /**
     * @param cpf the cpf to set
     */
    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    /**
     * @return the telefone
     */
    public String getTelefone() {
        return telefone;
    }

    /**
     * @param telefone the telefone to set
     */
    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    /**
     * @return the sexo
     */
    public String getSexo() {
        return sexo;
    }

    /**
     * @param sexo the sexo to set
     */
    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    /**
     * @return the endereco
     */
    public String getEndereco() {
        return endereco;
    }

    /**
     * @param endereco the endereco to set
     */
    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    /**
     * @return the senha_de_acesso
     */
    public String getSenha_de_acesso() {
        return senha_de_acesso;
    }

    /**
     * @param senha_de_acesso the senha_de_acesso to set
     */
    public void setSenha_de_acesso(String senha_de_acesso) {
        this.senha_de_acesso = senha_de_acesso;
    }

}